package com.example.doreqr;

public class Asistencia {

    String nombreAlumno;
    String hora;
    String fecha;
    String idAlumno;

    public Asistencia() {
    }

    public String getNombreAlumno() {
        return nombreAlumno;
    }

    public String getHora() {
        return hora;
    }

    public String getFecha() {
        return fecha;
    }

    public String getIdAlumno() {
        return idAlumno;
    }
}